"""BraTS-GLI 2023 dataset class built on MONAI CacheDataset.

Expected JSON structure (generated by split_generator.py):
    {
        "training": [
            {"image": ["t1n.nii.gz", "t1c.nii.gz", "t2w.nii.gz", "t2f.nii.gz"],
             "label": "seg.nii.gz"},
            ...
        ],
        "validation": [...]
    }

LoadImaged receives a list of 4 paths for "image" and stacks them into a
single 4-channel volume automatically.
"""

import json
from collections.abc import Callable
from pathlib import Path
from typing import Literal

from monai.data import CacheDataset, ThreadDataLoader


class BraTSDataset(CacheDataset):
    """CacheDataset subclass for BraTS-GLI 2023.

    Args:
        json_list: Path to the splits JSON file.
        split: "training" or "validation".
        transforms: MONAI Compose pipeline to apply.
        cache_rate: Fraction of samples to pre-load into CPU RAM.
        num_workers: Worker threads for the cache population step.
    """

    def __init__(
        self,
        json_list: str | Path,
        split: Literal["training", "validation"],
        transforms: Callable,
        cache_rate: float = 1.0,
        num_workers: int = 4,
    ) -> None:
        json_list = Path(json_list)
        if not json_list.exists():
            raise FileNotFoundError(
                f"Split file not found: {json_list}\n"
                "Run: python src/medseg_brats/data/split_generator.py --data_dir data/BraTS2023_GLI"
            )

        with open(json_list) as f:
            splits = json.load(f)

        if split not in splits:
            raise KeyError(f"Split '{split}' not found in {json_list}. Available: {list(splits)}")

        data = splits[split]

        super().__init__(
            data=data,
            transform=transforms,
            cache_rate=cache_rate,
            num_workers=num_workers,
        )


def build_loader(
    dataset: BraTSDataset,
    batch_size: int = 1,
    shuffle: bool = True,
    num_workers: int = 4,
) -> ThreadDataLoader:
    """Wrap a BraTSDataset in a ThreadDataLoader.

    ThreadDataLoader is used instead of torch DataLoader to avoid Windows
    multiprocessing fork issues with num_workers > 0.
    """
    return ThreadDataLoader(
        dataset,
        batch_size=batch_size,
        shuffle=shuffle,
        num_workers=num_workers,
        # pin_memory is not supported by ThreadDataLoader and causes issues on Windows
    )
